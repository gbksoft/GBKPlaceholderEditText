package com.example.placeholderedittext.util;

public class Distance {
	private int start = -1;
	private int end = -1;

	public void setStart(int start) {
		this.start = start;
	}

	public int getStart() {
		return start;
	}

	public void setEnd(int end) {
		this.end = end;
	}

	public int getEnd() {
		return end;
	}

}
